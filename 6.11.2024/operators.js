console.log("Assignment Operators in JS");
let c = 4;
console.log("c is: ",c);
c+=5; //c = c + 5
console.log("c is now(c+=5): ",c);
c-=5; //c = c - 5
console.log("c is now(c-=5): ",c);
c*=5; //c = c * 5
console.log("c is now(c*=5): ",c);
c/=5; //c = c / 5
console.log("c is now(c/=5): ",c);
c%=5; //c = c % 5
console.log("c is now(c%=5): ",c);
c**=5; //c = c ** 5
console.log("c is now(c**=5): ",c);

